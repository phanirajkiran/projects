//==========================================================
//	函数名称：	USART1_IRQHand
//
//	函数功能：	串口一485数据处理函数
//
//	入口参数：	无
//              
//	返回参数：	无
//
//	说明：		
//==========================================================

void USART1_IRQHand(void)
{
	char *strx1=0;										//接收对比结果
	char *strx2=0;										//接收对比结果
	char *strx3=0;										//接收对比结果
	uint16_t i = 0;										//for循环清空数组使用
	uint8_t len = 0;  								//存放接收到的数据长度
	Rs485_Config(0);  								//开启485接收

  if(USART1_RX_STA&0x8000)
	{
		len=USART1_RX_STA&0x3fff;			//得到此次接收到的数据长度

		strx1=strstr((const char*)USART1_RX_BUF,(const char*)"pit->lcd");			//判断总线数据是否为pitch传输给lcd的数据
		strx2=strstr((const char*)USART1_RX_BUF,(const char*)"rol->lcd");			//判断总线数据是否为roll传输给lcd的数据
		strx3=strstr((const char*)USART1_RX_BUF,(const char*)"yaw->lcd");			//判断总线数据是否为yaw传输给lcd的数据
		
		if(strx1)											//pitch数据
		{	
			switch(len)
			{
				case 9: 
						number1 = (USART1_RX_BUF[8]-0x30);
				break;
				case 10: 
						number1 = ((USART1_RX_BUF[9]-0x30)+(USART1_RX_BUF[8]-0x30)*10);
				break;
				case 11: 
						number1 = ((USART1_RX_BUF[10]-0x30) + (USART1_RX_BUF[9]-0x30)*10+(USART1_RX_BUF[8]-0x30)*100);
				break;
				case 12: 
						number1 = ((USART1_RX_BUF[11]-0x30)+ (USART1_RX_BUF[10]-0x30)*10 + (USART1_RX_BUF[9]-0x30)*100+(USART1_RX_BUF[8]-0x30)*1000);
				break;
				default : break;
			}
		}
		
		if(strx2)												//roll数据
		{	
			switch(len)
			{
				case 9: 
						number2 = (USART1_RX_BUF[8]-0x30);
				break;
				case 10: 
						number2 = ((USART1_RX_BUF[9]-0x30)+(USART1_RX_BUF[8]-0x30)*10);
				break;
				case 11: 
						number2 = ((USART1_RX_BUF[10]-0x30) + (USART1_RX_BUF[9]-0x30)*10+(USART1_RX_BUF[8]-0x30)*100);
				break;
				case 12: 
						number2 = ((USART1_RX_BUF[11]-0x30)+ (USART1_RX_BUF[10]-0x30)*10 + (USART1_RX_BUF[9]-0x30)*100+(USART1_RX_BUF[8]-0x30)*1000);
				break;
				default : break;
			}
		}
		
		if(strx3)											//yaw数据
		{	
			switch(len)
			{
				case 9: 
						number3 = (USART1_RX_BUF[8]-0x30);
				break;
				case 10: 
						number3 = ((USART1_RX_BUF[9]-0x30)+(USART1_RX_BUF[8]-0x30)*10);
				break;
				case 11: 
						number3 = ((USART1_RX_BUF[10]-0x30) + (USART1_RX_BUF[9]-0x30)*10+(USART1_RX_BUF[8]-0x30)*100);
				break;
				case 12: 
						number3 = ((USART1_RX_BUF[11]-0x30)+ (USART1_RX_BUF[10]-0x30)*10 + (USART1_RX_BUF[9]-0x30)*100+(USART1_RX_BUF[8]-0x30)*1000);
				break;
				default : break;
			}
		}	
				USART1_RX_STA=0;
				for(i = 0;i<200;i++){
					USART1_RX_BUF[i] = 0;						//清空数组
				}
	}
}


//==========================================================
//	函数名称：	TFT_Update
//
//	函数功能：	TFT显示数据更新
//
//	入口参数：	无
//              
//	返回参数：	无
//
//	说明：		
//==========================================================
void TFT_Update(void)
{
			int num1 = 0,num2 = 0,num3 = 0;
			
			delay_ms(10);														//延时等待数据稳定
	
			if(number1<0)
			{
					LCD_ShowString(75,50,ffhh); 				//pitch
					num1 = 0-number1;
					delay_ms(5);
					
			}
			else
			{
					LCD_ShowString(75,50,ffhh2); 				//pitch
					num1 =number1;
					delay_ms(5);
			}
			
			if(number2<0)
			{
					LCD_ShowString(75,100,ffhh); 				//roll
					num2 = 0-number2;
					delay_ms(5);
			}
			else
			{
					LCD_ShowString(75,100,ffhh2); 			//roll
					num2 =number2;
					delay_ms(5);
			}
			
			if(number3<0)
			{
					LCD_ShowString(75,150,ffhh); 				//yaw
					num3 = 0-number3;
					delay_ms(5);
			}
			else
			{
					LCD_ShowString(75,150,ffhh2); 			//yaw
					num3 =number3;
					delay_ms(5);
			}
			
			LCD_ShowNum(90,50,num1,5);							//pitch数据更新
			LCD_ShowNum(90,100,num2,5);							//roll数据更新
			LCD_ShowNum(90,150,num3,5);							//yaw数据更新

}

