int main(void)
{ 
  HAL_Init();             					//初始化HAL库  
	UART2_Init(115200);								//初始化串口2
	TIM3_Init(1000-1,64-1);						//初始化定时器3
	ESP8266_Init();										//初始化ESP8266

	while(1)
	{
			delay_ms(500);								//延时500ms使数据稳定
			printf("%d\r\n",28674031);		//发送数据到ONE.NET平台
	}
}




